function Search({ query, setQuery, onCloseMovie }) {
  const inputEl = useRef(null);
  useEffect(() => {
    // Set initial focus on the input element
    if (inputEl.current) {
      inputEl.current.focus();
    }
  }, []);

  useEffect(() => {
    // Define the keydown event callback
    function callback(e) {
      if (document.activeElement === inputEl.current) return; // Skip if already focused
      if (e.code === "Enter") {
        inputEl.current.focus(); // Focus input on Enter
        setQuery(""); // Reset query
        onCloseMovie();
      }
    }

    // Attach the event listener
    document.addEventListener("keydown", callback);

    // Cleanup event listener on unmount
    return () => document.removeEventListener("keydown", callback);
  }, [setQuery, onCloseMovie]);

  return (
    <input
      className="search"
      type="text"
      placeholder="Search movies..."
      value={query}
      onChange={(e) => setQuery(e.target.value)}
      ref={inputEl}
    />
  );
}

